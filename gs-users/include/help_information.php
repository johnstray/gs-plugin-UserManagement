<p style="text-align:justify;">An advanced user management system for GetSimple CMS. This great plugin has a never-before-seen groups management feature, and the ability for an unlimited number of integrated plugins. Some of the features include: Management of Individual Users for GetSimple admin areas, Support for User Profiles - With the option of Front-End Display, Never before seen* Group Management - Includes per group permissions, Custom Landing page selection which can be set on the global, group and user levels, Integration support with an unlimited number of other plugins^.</p>
